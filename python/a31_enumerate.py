def main():
    example_list = ['요소A', '요소B', '요소C']
    
    # enumerate를 사용하여 인덱스와 원소 출력
    for i, v in enumerate(example_list):
        print(f"{i} 번째 원소는 {v} 입니다❤️")  # 각 원소와 인덱스 출력

if __name__ == "__main__":  
    # 직접 실행 시 main 함수 호출
    # Call main function when executed directly  
    main()
